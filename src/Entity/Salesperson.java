package Entity;

public class Salesperson {

    private String name;
    private String cpf;
    private Double salary;

    public Salesperson() {

    }

    public void setName(String name) {
        this.name = name;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public void setSalary(Double salary) {
        this.salary = salary;
    }
}
